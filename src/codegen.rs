fn param_type(p: &Param, enclosing: Option<&str>) -> Type {
    match &p.kind {
        ParamKind::Named { ty, .. } => ty.clone(),
        ParamKind::SelfRef => Type::Ref(Box::new(self_type(enclosing.unwrap_or("")))),
        ParamKind::SelfMutRef => Type::MutRef(Box::new(self_type(enclosing.unwrap_or("")))),
        ParamKind::SelfMove => self_type(enclosing.unwrap_or("")),
    }
}

fn param_name(p: &Param) -> String {
    match &p.kind {
        ParamKind::Named { name, .. } => name.clone(),
        _ => "self".to_string(),
    }
}

fn receiver_struct(ty: &Type) -> Option<String> {
    match ty {
        Type::I64 => Some("i64".to_string()),
        Type::F64 => Some("f64".to_string()),
        Type::Named(n) | Type::Qualified(_, n) => Some(n.clone()),
        Type::Ref(inner) | Type::MutRef(inner) => receiver_struct(inner),
        _ => None,
    }
}

fn is_primitive_extend_target(name: &str) -> bool {
    matches!(name, "i64" | "f64")
}

fn self_type(name: &str) -> Type {
    match name {
        "i64" => Type::I64,
        "f64" => Type::F64,
        other => Type::Named(other.to_string()),
    }
}

fn is_struct_type(ty: &Type, cg: &CodeGen) -> bool {
    match ty {
        Type::Named(n) | Type::Qualified(_, n) => cg.structs.contains_key(n),
        _ => false,
    }
}

fn round_up(n: i64, mult: i64) -> i64 {
    if n <= 0 {
        return 0;
    }
    ((n + mult - 1) / mult) * mult
}

fn round_up_16(n: i64) -> i64 {
    round_up(n, 16)
}

fn sanitize(label: &str) -> String {
    label.replace(|c: char| !(c.is_ascii_alphanumeric() || c == '_'), "_")
}

fn nasm_byte_list(s: &str) -> String {
    let mut parts: Vec<String> = s.bytes().map(|b| b.to_string()).collect();
    parts.push("0".to_string());
    parts.join(", ")
}

/// Join asm token fragments into a single NASM line, inserting a separating
/// space only where required. NASM needs whitespace to separate a mnemonic from
/// word-like operands; commas, brackets, and `.label` pieces are self-delimiting.
fn join_asm_fragments(frags: &[String]) -> String {
    let mut out = String::new();
    for frag in frags {
        if frag.is_empty() {
            continue;
        }
        if out.is_empty() {
            out.push_str(frag);
            continue;
        }
        let prev = out.chars().last().unwrap();
        let next = frag.chars().next().unwrap();
        if asm_needs_space(prev, next) {
            out.push(' ');
        }
        out.push_str(frag);
    }
    out
}

fn asm_needs_space(prev: char, next: char) -> bool {
    // No space before these.
    if matches!(next, ',' | ':' | ']') {
        return false;
    }
    // No space after these.
    if matches!(prev, '[' | '.') {
        return false;
    }
    true
}
